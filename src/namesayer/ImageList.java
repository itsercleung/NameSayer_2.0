package namesayer;

public class ImageList {

    private String image;
    private String flag;

    public ImageList(String image, String flag) {
        this.image = image;
        this.flag = flag;
    }

    public String getImage() {
        return this.image;
    }

    public String getFlag() {
        return this.flag;
    }

    public String toString() {
        return this.flag;
    }
}